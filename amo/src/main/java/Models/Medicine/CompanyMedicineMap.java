package Models.Medicine;

public class CompanyMedicineMap {
	private int compID;
	private String medBCode;
	private String mapStatus;

	public int getCompID() {
		return compID;
	}

	public void setCompID(int compID) {
		this.compID = compID;
	}

	public String getMedBCode() {
		return medBCode;
	}

	public void setMedBCode(String medBCode) {
		this.medBCode = medBCode;
	}

	public String getMapStatus() {
		return mapStatus;
	}

	public void setMapStatus(String mapStatus) {
		this.mapStatus = mapStatus;
	}
}
