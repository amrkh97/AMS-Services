package Models.AmbulanceMap;

public class AllAmbulanceMapDataModel {

	private Integer vin;
	private String licensePlate;
	private String make;
	private String paramedicName;
	private Integer paramedicID;
	private String driverName;
	private Integer driverID;
	private String yelloPadUniqueID;

	public Integer getVin() {
		return vin;
	}

	public void setVin(Integer vin) {
		this.vin = vin;
	}

	public String getLicensePlate() {
		return licensePlate;
	}

	public void setLicensePlate(String licensePlate) {
		this.licensePlate = licensePlate;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

	public String getParamedicName() {
		return paramedicName;
	}

	public void setParamedicName(String paramedicName) {
		this.paramedicName = paramedicName;
	}

	public Integer getParamedicID() {
		return paramedicID;
	}

	public void setParamedicID(Integer paramedicID) {
		this.paramedicID = paramedicID;
	}

	public String getDriverName() {
		return driverName;
	}

	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}

	public Integer getDriverID() {
		return driverID;
	}

	public void setDriverID(Integer driverID) {
		this.driverID = driverID;
	}

	public String getYelloPadUniqueID() {
		return yelloPadUniqueID;
	}

	public void setYelloPadUniqueID(String yelloPadUniqueID) {
		this.yelloPadUniqueID = yelloPadUniqueID;
	}

}
